function renderLeaderboard() {
    let html = "<ol>";
    leaderboard.forEach(p => {
        html += `<li>${p.name} (${p.category}) - ${p.score}</li>`;
    });
    html += "</ol>";

    document.getElementById("leaderboard").innerHTML = html;
}

